#include "libft.h"

char    *ft_putchar_2(char c)
{
    char *s;

    if (c == 0)
    	write(1, "\0", 1);
	s = ft_strnew(1);
    s[0] = c;
    return (s);
}

void	ft_putchar(char c)
{
	write(1, &c, 1);
}
